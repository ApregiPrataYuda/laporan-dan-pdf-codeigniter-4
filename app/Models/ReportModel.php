    public function getTransactionsByPeriod($startDate, $endDate)
    {
        // Query untuk mengambil transaksi berdasarkan periode
        $builder = $this->db->table($this->table);
        $builder->select('*');
        $builder->where('tgl >', $startDate);
        $builder->where('tgl <', $endDate);
        $query = $builder->get();

        return $query->getResultArray();
    }
